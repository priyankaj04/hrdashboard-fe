// Generate comprehensive attendance data for all employees
const generateAttendanceData = () => {
  const attendance = [];
  let recordId = 1;
  
  // Define months to generate data for (last 3 months)
  const months = [
    { year: 2024, month: 7, days: 31 }, // July 2024
    { year: 2024, month: 8, days: 31 }, // August 2024
    { year: 2024, month: 9, days: 30 }, // September 2024
  ];
  
  // Define possible check-in times and statuses
  const checkInTimes = ['08:30', '08:45', '09:00', '09:15', '09:30'];
  const statuses = ['present', 'late', 'absent', 'half-day'];
  const statusWeights = [0.75, 0.15, 0.05, 0.05]; // 75% present, 15% late, 5% absent, 5% half-day
  
  // Generate attendance for each employee for each working day
  for (let empId = 1; empId <= 35; empId++) {
    months.forEach(({ year, month, days }) => {
      for (let day = 1; day <= days; day++) {
        const date = new Date(year, month - 1, day);
        // Skip weekends (Saturday = 6, Sunday = 0)
        if (date.getDay() === 0 || date.getDay() === 6) continue;
        
        // Random status based on weights
        const rand = Math.random();
        let status = 'present';
        let cumWeight = 0;
        for (let i = 0; i < statuses.length; i++) {
          cumWeight += statusWeights[i];
          if (rand < cumWeight) {
            status = statuses[i];
            break;
          }
        }
        
        const dateStr = `${year}-${month.toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}`;
        let checkIn = '';
        let checkOut = '';
        let totalHours = 0;
        
        if (status === 'present') {
          checkIn = checkInTimes[Math.floor(Math.random() * checkInTimes.length)];
          const checkInHour = parseInt(checkIn.split(':')[0]);
          const checkInMin = parseInt(checkIn.split(':')[1]);
          const checkOutHour = checkInHour + 8 + Math.floor(Math.random() * 2); // 8-9 hours later
          const checkOutMin = checkInMin + (Math.random() > 0.5 ? 30 : 0);
          checkOut = `${checkOutHour}:${checkOutMin.toString().padStart(2, '0')}`;
          totalHours = ((checkOutHour * 60 + checkOutMin) - (checkInHour * 60 + checkInMin)) / 60;
        } else if (status === 'late') {
          checkIn = '09:' + (15 + Math.floor(Math.random() * 45)).toString().padStart(2, '0'); // 9:15-10:00
          const checkInHour = parseInt(checkIn.split(':')[0]);
          const checkInMin = parseInt(checkIn.split(':')[1]);
          const checkOutHour = checkInHour + 8 + Math.floor(Math.random() * 2);
          const checkOutMin = checkInMin + (Math.random() > 0.5 ? 30 : 0);
          checkOut = `${checkOutHour}:${checkOutMin.toString().padStart(2, '0')}`;
          totalHours = ((checkOutHour * 60 + checkOutMin) - (checkInHour * 60 + checkInMin)) / 60;
        } else if (status === 'half-day') {
          checkIn = checkInTimes[Math.floor(Math.random() * checkInTimes.length)];
          const checkInHour = parseInt(checkIn.split(':')[0]);
          const checkInMin = parseInt(checkIn.split(':')[1]);
          const checkOutHour = checkInHour + 4; // 4 hours for half day
          checkOut = `${checkOutHour}:${checkInMin.toString().padStart(2, '0')}`;
          totalHours = 4;
        }
        // For absent, checkIn and checkOut remain empty, totalHours = 0
        
        attendance.push({
          id: recordId.toString(),
          employee_id: empId.toString(),
          employeeId: empId.toString(), // Keep both for compatibility
          date: dateStr,
          check_in: checkIn,
          check_out: checkOut,
          checkIn: checkIn, // Keep both for compatibility
          checkOut: checkOut, // Keep both for compatibility
          total_hours: Math.round(totalHours * 100) / 100,
          totalHours: Math.round(totalHours * 100) / 100, // Keep both for compatibility
          status: status
        });
        
        recordId++;
      }
    });
  }
  
  return attendance;
};

